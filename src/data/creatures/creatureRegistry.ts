export const CREATURES: Record<CreatureId, CreatureSpecies> = {
  feeder_cricket: FEEDER_CRICKET,
  colorful_frog: COLORFUL_FROG,
  cherry_shrimp: CHERRY_SHRIMP,
  nerite_snail: NERITE_SNAIL,
  neon_tetra: NEON_TETRA,
  guppy: GUPPY,
  zebra_danio: ZEBRA_DANIO,
  otocinclus: OTOCINCLUS,
  mystery_snail: MYSTERY_SNAIL,
  daphnia: DAPHNIA,
  isopod: ISOPOD,
};

export function creatureList(): CreatureSpecies[] {
  return Object.values(CREATURES);
}

export function getCreature(id: CreatureId): CreatureSpecies {
  const c = CREATURES[id];
  if (!c) throw new Error(`Unknown creature id: ${id}`);
  return c;
}

export function aquariumCreatures(): CreatureSpecies[] {
  return creatureList().filter((c) => c.habitatType === "aquarium");
}

export function vivariumCreatures(): CreatureSpecies[] {
  return creatureList().filter((c) => c.habitatType === "vivarium");
}

/** The dev/test aquarium population: every aquatic species at its default
 *  (group-respecting) count. */
export function defaultAquariumPopulation(): { id: CreatureId; count: number }[] {
  return aquariumCreatures().map((c) => ({ id: c.id, count: c.spawn.defaultCount }));
}

/** Final part-role map for a creature: the spatial classifier's read of the
 *  measured parts, with the registry's hand-authored corrections on top. */
export function resolvePartRoles(id: CreatureId, parts: ClassifiablePart[]): Record<string, PartRole> {
  const c = getCreature(id);
  return {
    ...classifyParts(parts, c.asset.forward, {
      shellCreature: c.asset.shellCreature,
      hasLegs: c.asset.hasLegs,
    }),
    ...c.asset.partOverrides,
  };
}
